# datatype.py

# bool

a = True
b = False

print("datatype [bool]")
print("a =", a)
print("b =", b)
print("type(a) =", type(a))
print("\n")

# int and float

a = 1
b = 2.0

print("datatype [int]")
print("a =", a)
print("type(a) =", type(a))
print("\n")

print("datatype [float]")
print("b =", b)
print("type(b) =", type(b))
print("\n")

c = a+b
print("c = a+b")
print("c =", c)
print("type(c)", type(c))
print("\n")

